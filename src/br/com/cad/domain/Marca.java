package br.com.cad.domain;

import java.io.Serializable;
import java.util.Objects;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;
import org.hibernate.validator.constraints.Length;

/**
 *
 * @author eveton
 */
@Entity
@Table(name = "tbl_marca")
public class Marca implements Serializable {
    
    @Id
    @SequenceGenerator(name = "seq_marca",sequenceName = "seq_marca_id",allocationSize = 1)
    @GeneratedValue(generator = "seq_marca",strategy = GenerationType.SEQUENCE)
    private Integer id;
    
    @Column(name = "nome_marca",nullable = false,length = 80)
    @Length(max = 80,message = "Nome da marca n√£o pode ultrapassar {80} caracteres")
    private String nome;

    public Marca() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    @Override
    public int hashCode() {
        int hash = 5;
        hash = 17 * hash + Objects.hashCode(this.id);
        hash = 17 * hash + Objects.hashCode(this.nome);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Marca other = (Marca) obj;
        if (!Objects.equals(this.nome, other.nome)) {
            return false;
        }
        if (!Objects.equals(this.id, other.id)) {
            return false;
        }
        return true;
    }

    
}
